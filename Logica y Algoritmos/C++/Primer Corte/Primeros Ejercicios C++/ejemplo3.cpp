/*Sumar dos numeros*/
#include <iostream>
using namespace std;

int main(int argc, char const *argv[])
{
   int num1 = 0, num2 = 0, sum;
   cout<<"Ingrese un numero para sumarlo"<<endl;
   cin>>num1;
   cout<<"Ingrese otro numero"<<endl;
   cin>>num2;
   sum = num1 + num2;
   cout<<"La suma de los numeros es: "<< sum << endl;;
    return 0;
}
